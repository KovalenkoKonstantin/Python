# Для того, чтобы узнать длину строки,
# можно использовать встроенную функцию len()
#
# Эта функция возвращает количество символов в строке.
s = "Hello world"
print(len(s))

# А если строка, в которой нам нужно посчитать количество символов
# изначально не известна (например если мы считываем эту строку с помощью input()
text = input()
print(len(text))


