# Для того чтобы превратить строку в число,
# можно использовать встроенную функцию int()
a = int("5")
b = int("7")
print(a + b)

# число в строку встроенная функция str()
a = str(5)
b = str(7)
print(a + b)

print(int("10") + int("5"))

print(str(5) + str(10))

