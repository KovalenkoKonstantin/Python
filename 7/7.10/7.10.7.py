l = [1, 2, 3, 4, 5]
print(l[::-1][::-1]) # Yes
# print(l[::-1]) # [5, 4, 3, 2, 1]

l = [1, 2, 3, 4, 5]
print(l[:7]) # Yes

l = [1, 2, 3, 4, 5]
print(l[5::-1][5::-1]) # Yes
# print(l[5::-1]) # [5, 4, 3, 2, 1]

