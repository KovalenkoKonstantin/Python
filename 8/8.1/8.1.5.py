# К элементам списка, находящегося внутри другого списка также возможно обратиться:
l = [[1, 2, 3], [4, 5, 6]] # Элементами списка l являются списки [1, 2, 3] и [4, 5, 6]
print(l[0][0]) # обращаемся к элементу с индексом 0 списка [1, 2, 3]
print(l[0][1]) # обращаемся к элементу с индексом 1 списка [1, 2, 3]
